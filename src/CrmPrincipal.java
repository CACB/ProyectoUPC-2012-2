    //  Metodo para cargar la grilla
    private void cargar() throws BusinessException {
        ArrayList<Cliente> cli = new ArrayList<Cliente>();
        model = new DefaultTableModel(null, columnas);
        
        cli = crm.Fn_MostrarClientes("","","","","","","");
        for (int i = 0; i < cli.size(); i++) {
            String[] prue = {cli.get(i).getNombres(), cli.get(i).getApellidopat(), cli.get(i).getApellidomat(), cli.get(i).getEmail(), cli.get(i).getDni(), cli.get(i).getTelefono(), cli.get(i).getFechaContacto(), String.valueOf(cli.get(i).getEstado())};
            model.addRow(prue);
        }

        jGrid.setModel(model);
    }
